  // Formatar n√∫mero de telefone
  formatPhoneNumber(phone) {
    if (!phone) return '';

    // Remove todos os caracteres n√£o num√©ricos
    let cleanPhone = phone.replace(/\D/g, '');

    // Verifica se o n√∫mero tem pelo menos 10 d√≠gitos
    if (cleanPhone.length < 10) {
      console.warn(`‚ö†Ô∏è N√∫mero inv√°lido: ${phone}`);
      return '';
    }

    // Se n√£o come√ßar com 55 (Brasil), adiciona
    if (!cleanPhone.startsWith('55')) {
      // Se come√ßar com 0, remove
      if (cleanPhone.startsWith('0')) {
        cleanPhone = cleanPhone.substring(1);
      }
      // Adiciona c√≥digo do Brasil
      cleanPhone = '55' + cleanPhone;
    }

    return cleanPhone;
  }

  // Calcular dias em atraso
  calculateDaysOverdue(dueDate) {
    const today = new Date();
    const due = new Date(dueDate);
    const diffTime = today.getTime() - due.getTime();
    const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
    return diffDays > 0 ? diffDays : 0;
  }

  // Calcular dias at√© vencimento
  calculateDaysUntil(dueDate) {
    const today = new Date();
    const due = new Date(dueDate);
    const diffTime = due.getTime() - today.getTime();
    const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));

    if (diffDays === 0) return 'hoje';
    if (diffDays === 1) return '1 dia';
    if (diffDays > 1) return `${diffDays} dias`;
    return 'vencida';
  }

  // Validar cliente para WhatsApp
  validateClientWhatsApp(client) {
    if (!client.phone) {
      return { valid: false, error: 'Cliente n√£o possui telefone cadastrado' };
    }

    const cleanPhone = this.formatPhoneNumber(client.phone);
    if (!cleanPhone) {
      return { valid: false, error: 'Telefone inv√°lido' };
    }

    return { valid: true, phone: cleanPhone };
  }
